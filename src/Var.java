public class Var {
    public static void main(String[] args) {
        String name;
        name = "Nik Akif";
        System.out.println(name);

        int age = 24;
        String address = "Bangi";

        System.out.println(age);
        System.out.println(address);

        name = "Ahmad Akif";
        System.out.println(name);

        // var just like JavaScript and PHP
        var number = 1;
        var nama = "Akif";

        System.out.println(number + nama);

        //var must first be initialize;
        // cannot var name;


    }
}
